    /// <summary>
    /// Air Bag Consts.
    /// </summary>
    public static class AirBagConsts
    {
        #region Folders
        
        /// <summary>
        /// Gets Share config folder.
        /// </summary>
        public static string ShareConfigFolder
        {
            get
            {
                return ApplicationManager.Instance.Environments.Company.Configs.FullName;
            }
        }

        #endregion

        #region Public Methods
        
        /// <summary>
        /// Gets Config Full File Name.
        /// </summary>
        /// <param name="configFileName">The config file name.</param>
        /// <returns>Returns The config Full File Name.</returns>
        public static string GetShareConfigFullName(string configFileName)
        {
            return Path.Combine(ShareConfigFolder, configFileName);
        }

        #endregion
    }

